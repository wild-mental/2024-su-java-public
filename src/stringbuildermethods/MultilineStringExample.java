package stringbuildermethods;

public class MultilineStringExample {
    public static void main(String[] args) {
        // Using triple double-quoted string for multiline text
        String multilineText = """
            This is a multiline
            string literal in Java.
            It allows you to easily
            write text spanning
            multiple lines.
            """;

        // Print the multiline text
        System.out.println(multilineText);
    }
}